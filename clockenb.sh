#!/bin/bash
#	Set main clock buffer via I2C
#
ba=$1
if [ $ba"x" == "x" ] ; then ba="0" ; fi 
#	Output 8 - ACLK[3:2]
./vmebur -q "m $ba 2000000;2000C=50000000;20010=90000000;w 100;2000C=7000000;20010=10000000;w 100;\
2000C=2000000;20010=10000000;w 100;2000C=18000000;20010=50000000"
./vmebur -q "m $ba 2000000;2000C=50000000;20010=90000000;w 100;2000C=7000000;20010=10000000;w 100;\
2000C=51000000;20010=90000000;w 100;20010=20000000;w 100;2000C;20010=68000000;w 100;2000C"
#	Output 7 - ACLK[5:4]
./vmebur -q "m $ba 2000000;2000C=50000000;20010=90000000;w 100;2000C=6000000;20010=10000000;w 100;\
2000C=2000000;20010=10000000;w 100;2000C=18000000;20010=50000000"
./vmebur -q "m $ba 2000000;2000C=50000000;20010=90000000;w 100;2000C=6000000;20010=10000000;w 100;\
2000C=51000000;20010=90000000;w 100;20010=20000000;w 100;2000C;20010=68000000;w 100;2000C"
#	Output 6 - ACLK[7:6]
./vmebur -q "m $ba 2000000;2000C=50000000;20010=90000000;w 100;2000C=5000000;20010=10000000;w 100;\
2000C=2000000;20010=10000000;w 100;2000C=18000000;20010=50000000"
./vmebur -q "m $ba 2000000;2000C=50000000;20010=90000000;w 100;2000C=5000000;20010=10000000;w 100;\
2000C=51000000;20010=90000000;w 100;20010=20000000;w 100;2000C;20010=68000000;w 100;2000C"
#	Output 5 - ACLK[9:8]
./vmebur -q "m $ba 2000000;2000C=50000000;20010=90000000;w 100;2000C=4000000;20010=10000000;w 100;\
2000C=2000000;20010=10000000;w 100;2000C=18000000;20010=50000000"
./vmebur -q "m $ba 2000000;2000C=50000000;20010=90000000;w 100;2000C=4000000;20010=10000000;w 100;\
2000C=51000000;20010=90000000;w 100;20010=20000000;w 100;2000C;20010=68000000;w 100;2000C"
#	Output 4 - ECLK
./vmebur -q "m $ba 2000000;2000C=50000000;20010=90000000;w 100;2000C=3000000;20010=10000000;w 100;\
2000C=2000000;20010=10000000;w 100;2000C=18000000;20010=50000000"
./vmebur -q "m $ba 2000000;2000C=50000000;20010=90000000;w 100;2000C=3000000;20010=50000000;w 100;\
2000C=51000000;20010=90000000;w 100;20010=20000000;w 100;2000C;20010=68000000;w 100;2000C"
#	Configure Si5338
./vmebur -q "m $ba 2000000;k 0 Si5338-125MHz.h;k 1 Si5338-125MHz.h;k 2 Si5338-125MHz.h;k 3 Si5338-125MHz.h;"
